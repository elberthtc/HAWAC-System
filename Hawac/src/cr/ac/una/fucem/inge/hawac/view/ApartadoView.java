    private void primaTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_primaTextKeyReleased
        if (primaText.getText().length() != 0) {
            try {
                if (Model.isNumeric(primaText.getText())) {
                    int abonoInt = Integer.parseInt(primaText.getText());
                    if(abonoInt>model.getCurrent().getP().getPrecio()){
                        saldoText.setText("Ha excedido el precio del producto.");
                    }else if(abonoInt<0){
                        saldoText.setText("Ha ingresado un nÃºmero negativo.");
                    } else
                        saldoText.setText(model.getCurrent().getP().getPrecio() - abonoInt + "");
                } else if (Model.isNumeric2(primaText.getText())) {
                    double abono = Double.parseDouble(primaText.getText());
                    if(abono>model.getCurrent().getP().getPrecio()){
                        saldoText.setText("Ha excedido el precio del producto.");
                    }else if(abono<0){
                        saldoText.setText("Ha ingresado un nÃºmero negativo.");
                    } else
                        saldoText.setText(model.getCurrent().getP().getPrecio() - abono + "");
                }else
                    saldoText.setText("Ha ingresado valores no numericos");
            } catch (Exception e) {
                saldoText.setText("Error");
            }
        }else{
            saldoText.setText(model.getCurrent().getP().getPrecio() + "");
        }
    }//GEN-LAST:event_primaTextKeyReleased

    @Override
    public void update(java.util.Observable updatedModel, Object parametros) {
        Apartado current = model.getCurrent();
        //this.dependienteText.setEnabled(model.getModo() == Application.MODO_AGREGAR);
        
        current.setU(Application.USUARIO);
        if(current.getU() != null)
            current.setUsuario(Application.USUARIO.getIdUsuario());
        dependienteText.setEnabled(false);
        if(current.getU() != null)
            dependienteText.setText(current.getU().getNombre());
        else
            dependienteText.setText("");
        clienteText.setEnabled(false);
        
        
        if(current.getC().getNombre().length()==0)
            this.clienteText.setText("");
        else
            this.clienteText.setText(current.getC().getNombre());
        if (model.getErrores().get("Cliente") != null) {
            clienteLabel.setBorder(Application.BORDER_ERROR);
            clienteLabel.setToolTipText(model.getErrores().get("Cliente"));
        } else {
            clienteLabel.setBorder(null);
            clienteLabel.setToolTipText("");
        }
        
        Boolean editable = Arrays.asList(Application.MODO_AGREGAR, Application.MODO_EDITAR).contains(model.getModo());
        
        
        this.fecha_limiteText.setEnabled(false);
        this.fecha_limiteText.setText(current.getFechaLimite2());
        if (model.getErrores().get("Limite") != null) {
            limiteLabel.setBorder(Application.BORDER_ERROR);
            limiteLabel.setToolTipText(model.getErrores().get("Limite"));
        } else {
            limiteLabel.setBorder(null);
            limiteLabel.setToolTipText("");
        }
        
        this.fecha_realizadoText.setEnabled(false);
        this.fecha_realizadoText.setText(current.getFechaRealizado2());
        if (model.getErrores().get("Realizado") != null) {
            realizadoLabel.setBorder(Application.BORDER_ERROR);
            realizadoLabel.setToolTipText(model.getErrores().get("Realizado"));
        } else {
            realizadoLabel.setBorder(null);
            realizadoLabel.setToolTipText("");
        }
        
        this.productoText.setEnabled(false);
        this.productoText.setText(current.getP().getDescripcion());
        if (model.getErrores().get("Producto") != null) {
            productoLabel.setBorder(Application.BORDER_ERROR);
            productoLabel.setToolTipText(model.getErrores().get("Producto"));
        } else {
            productoLabel.setBorder(null);
            productoLabel.setToolTipText("");
        }
        
        this.primaText.setText("");
        if (model.getErrores().get("Prima") != null) {
            primaLabel.setBorder(Application.BORDER_ERROR);
            primaLabel.setToolTipText(model.getErrores().get("Prima"));
        } else {
            primaLabel.setBorder(null);
            primaLabel.setToolTipText("");
        }
        
        saldoText.setEnabled(false);
        
        try{
            this.saldoText.setText(current.getP().getPrecio() + "");
        } catch (Exception e){
            this.saldoText.setText(e.toString());
        }
        
        AgregarButton.setVisible(editable);
        this.validate();
        if (!model.getMensaje().equals("")) {
            JOptionPane.showMessageDialog(this, model.getMensaje(), "", JOptionPane.INFORMATION_MESSAGE);
        }
    }
